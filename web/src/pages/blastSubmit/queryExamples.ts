import type { BlastProgram } from "@/api/endpoints";

export interface QueryExampleTemplate {
  id: string;
  label: string;
  group: string;
  description: string;
  sourceFile: string;
  /** Free-text recommendation displayed on the card; superseded by `matchingDbs` for filtering. */
  recommendedDb?: string;
  /**
   * BLAST database base names (matching `DB_DESCRIPTIONS` keys) this example
   * produces meaningful hits against. Drives the "selected DB only" filter
   * in the example picker.
   */
  matchingDbs: string[];
  /** Program automatically applied when this example is loaded. */
  blastProgram: BlastProgram;
  sequenceCount: number;
  length: number;
  fasta: string;
}

export function queryExamplesForDatabase(
  examples: QueryExampleTemplate[],
  dbName: string,
): QueryExampleTemplate[] {
  const selectedDbName = dbName.trim();
  if (!selectedDbName) return [];
  return examples.filter((example) => example.matchingDbs.includes(selectedDbName));
}

